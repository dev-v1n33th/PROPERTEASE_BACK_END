package com.arshaa.common;

import java.util.Date;

public class CheckOutIntiated {

	private String email;
	private String name;
	private Date checkOutIntiatedDate;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		name = name;
	}

	public Date getCheckOutIntiatedDate() {
		return checkOutIntiatedDate;
	}

	public void setCheckOutIntiatedDate(Date checkOutIntiatedDate) {
		this.checkOutIntiatedDate = checkOutIntiatedDate;
	}

}
